@propertyWrapper
struct CachedData {
    lazy var value: String = self.initializer()
    let initializer: () -> String
    
    init(wrappedValue initializer: @escaping @autoclosure () -> String) {
        self.initializer = initializer
    }
    
    var wrappedValue: String {
        mutating get {
            value
        }
        set {
            value = newValue
        }
    }
}

