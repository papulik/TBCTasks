enum TarotCard {
    case Star
    case Empress
    case Tower
    case HangedMan
    case Chariot
    case Moon
    case Hierophant
    case Devil
    case Lovers
    case WheelOfFortune
    case Fool
    case Magician
    
    // рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃАрЃбрЃљрЃбрЃўрЃЎрЃБрЃарЃў рЃЏрЃћрЃЌрЃЮрЃЊрЃў TarotCard-рЃерЃў рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃЊрЃљрЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃЎрЃљрЃарЃбрЃА рЃерЃћрЃарЃЕрЃћрЃЋрЃўрЃЌрЃљрЃЊ
    
    static let allCards = [Star, Empress, Tower, HangedMan, Chariot, Moon, Hierophant, Devil, Lovers, WheelOfFortune, Fool, Magician]
    
